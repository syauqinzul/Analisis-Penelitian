a, b, c, d = koef
print(f"Model: y = {a:.6f}*x1 + {b:.6f}*x2 + {c:.6f}*x3 + {d:.6f}*x4")
# Output: y = 0.178707*x1 + 0.006183*x2 + 0.000121*x3 + -0.009533*x4

 
# ============================================================
# PREDIKSI DAN EVALUASI
# ============================================================
Y_pred = X @ koef
error  = Y - Y_pred
 
MAE  = np.mean(np.abs(error))
MAPE = np.mean(np.abs(error / Y)) * 100
SST  = np.sum((Y - np.mean(Y))**2)
SSE  = np.sum(error**2)
R2   = 1 - SSE/SST
RMSE = np.sqrt(np.mean(error**2))
 
print(f"MAE  = {MAE:.8f} kWh/m2/hari")
print(f"MAPE = {MAPE:.6f} %")
print(f"R2   = {R2:.8f}")
print(f"RMSE = {RMSE:.8f}")

