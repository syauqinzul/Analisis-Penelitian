# ============================================================
# ESTIMASI DAYA PANEL SURYA (Model Fotovoltaik)
# y = G * eta_STC * [1 - beta*(T - T_STC)]
# ============================================================
eta_STC = 0.18   # Efisiensi panel STC (18%)
beta_T  = 0.004  # Koefisien temperatur (0.4%/C)
T_STC   = 25.0   # Suhu kondisi standar (C)
 
Y = ALLSKY * eta_STC * (1 - beta_T * (T2M - T_STC))
 
# ============================================================
# ALJABAR LINEAR: MODEL OLS 4 VARIABEL
# X = [x1, x2, x3, x4] in R^(60x4)
# Y = X * beta  =>  beta = (X'X)^-1 * X'Y
# ============================================================
X = np.column_stack([ALLSKY, T2M, WS10M, QV2M])
 
XtX  = X.T @ X        # (4x4)
XtY  = X.T @ Y        # (4,)
